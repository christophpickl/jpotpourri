package net.sourceforge.jpotpourri.jpotface;


/**
 * 
 * @author christoph_pickl@users.sourceforge.net
 */
public final class PtWidthHeight {
    
	private final int width;
    
	private final int height;
    
	
    public PtWidthHeight(final int width, final int height) {
        this.width = width;
        this.height = height;
    }
    
    public int getHeight() {
        return this.height;
    }
    
    public int getWidth() {
        return this.width;
    }

}
